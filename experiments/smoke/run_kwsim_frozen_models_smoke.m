%% RUN_KWSIM_FROZEN_MODELS_SMOKE
% Apply frozen adaptive-REQ models to one exported KWSIM field.

clear;
clc;

this_file = mfilename("fullpath");
smoke_directory = fileparts(this_file);
experiments_directory = fileparts(smoke_directory);
repository_root = fileparts(experiments_directory);

addpath(repository_root);

repository_root = setup_adaptive_req();

sample_file = string( ...
    getenv("KWSIM_REQ_SAMPLE"));

if strlength(sample_file) == 0
    error( ...
        "adaptive_req:MissingKwsimSampleEnvironment", ...
        "Set KWSIM_REQ_SAMPLE before running this smoke test.");
end

bundle_file = string( ...
    getenv("ADAPTIVE_REQ_MODEL_BUNDLE"));

if strlength(bundle_file) == 0
    bundle_file = fullfile( ...
        repository_root, ...
        "outputs", ...
        "baseline_minimal_v1", ...
        "models", ...
        "baseline_minimal_v1_q_models.mat");
end

timestamp = string(datetime( ...
    "now", ...
    "Format", ...
    "yyyyMMdd_HHmmss"));

output_directory = fullfile( ...
    repository_root, ...
    "outputs", ...
    "smoke", ...
    "kwsim_frozen_models", ...
    timestamp);

outcome = ...
    adaptive_req.integration. ...
    evaluate_kwsim_frozen_models( ...
        sample_file, ...
        bundle_file, ...
        M=2, ...
        CsGuessMPerS=3.0, ...
        TargetStepM=1e-3, ...
        OutputDirectory=output_directory, ...
        UseWindowParfor=false);

fprintf("\n============================================\n");
fprintf("KWSIM frozen-model smoke completed\n");
fprintf("============================================\n");

disp(outcome.metrics);

fprintf("Output directory:\n%s\n", ...
    output_directory);
