%% RUN_KWSIM_REQ_SMOKE
% Verify that one exported KWSIM field enters the project-native REQ code.

clear;
clc;

this_file = mfilename("fullpath");
smoke_directory = fileparts(this_file);
experiments_directory = fileparts(smoke_directory);
repository_root = fileparts(experiments_directory);

addpath(repository_root);

repository_root = setup_adaptive_req();

sample_file = string( ...
    getenv("KWSIM_REQ_SAMPLE"));

if strlength(sample_file) == 0
    error( ...
        "adaptive_req:MissingKwsimSampleEnvironment", ...
        ["Set KWSIM_REQ_SAMPLE to the exported " ...
         "req_validation_sample.mat file."]);
end

timestamp = string(datetime( ...
    "now", ...
    "Format", ...
    "yyyyMMdd_HHmmss"));

output_directory = fullfile( ...
    repository_root, ...
    "outputs", ...
    "smoke", ...
    "kwsim_req", ...
    timestamp);

outcome = ...
    adaptive_req.integration. ...
    evaluate_kwsim_req_smoke( ...
        sample_file, ...
        M=2, ...
        CsGuessMPerS=3.0, ...
        TargetStepM=1e-3, ...
        OutputDirectory=output_directory, ...
        UseWindowParfor=false);

fprintf("\n============================================\n");
fprintf("KWSIM -> REQ smoke integration completed\n");
fprintf("============================================\n");

fprintf("%s\n", ...
    outcome.report.summary);

fprintf("Output directory:\n%s\n", ...
    output_directory);
