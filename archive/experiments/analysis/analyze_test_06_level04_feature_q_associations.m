%% analyze_test_06_level04_feature_q_associations.m
% Level 4 feature-q association analysis.
%
% This script quantifies whether local spectral features are associated with
% q, globally and within controlled parameter groups.
%
% It does not assume that the relation is linear. It computes Pearson,
% Spearman, Kendall, linear-fit diagnostics, and binned trends.

clear; clc; close all;
format compact;

%% Project setup

this_file = mfilename('fullpath');
this_dir = fileparts(this_file);
root_dir = fileparts(fileparts(this_dir));

addpath(root_dir);
root_dir = setup_adaptive_req();

%% Load MC results

experiment_name = 'test_06_feature_q_baseline';

[T_mc, MC, PATHS] = adaptive_req.analysis.load_mc_results( ...
    experiment_name, ...
    'RootDir', root_dir, ...
    'Verbose', true);

%% Detect columns and features

COL = adaptive_req.analysis.detect_mc_columns(T_mc);

feature_vars = adaptive_req.analysis.detect_feature_variables( ...
    T_mc, ...
    'Verbose', true);

if isempty(feature_vars)
    error('No local feature variables were detected.');
end

%% Build step summary

T_step = adaptive_req.analysis.make_step_summary_table( ...
    T_mc, ...
    COL, ...
    feature_vars);

fprintf('\nT_step rows: %d\n', height(T_step));

%% Output folder

analysis_dir = fullfile(PATHS.analysis_dir, 'level_04_feature_q_associations');

if ~exist(analysis_dir, 'dir')
    mkdir(analysis_dir);
end

%% 1. Global association

[T_assoc_global, T_binned_global] = ...
    adaptive_req.analysis.compute_groupwise_feature_q_associations( ...
        T_step, ...
        feature_vars, ...
        'QVar', 'q_mean', ...
        'GroupVars', string.empty(1, 0), ...
        'NumBins', 8, ...
        'BinMode', 'equal_count', ...
        'MinN', 8, ...
        'Verbose', true);

%% 2. Association grouped by WaveModel and M

[T_assoc_by_model_M, T_binned_by_model_M] = ...
    adaptive_req.analysis.compute_groupwise_feature_q_associations( ...
        T_step, ...
        feature_vars, ...
        'QVar', 'q_mean', ...
        'GroupVars', ["SIM_WaveModel", "REQ_M"], ...
        'NumBins', 6, ...
        'BinMode', 'equal_count', ...
        'MinN', 8, ...
        'Verbose', true);

%% 3. Association within controlled physical-estimator groups

controlled_group_vars = [ ...
    "SIM_WaveModel", ...
    "REQ_M", ...
    "SIM_f0", ...
    "SIM_cs_bg"];

[T_assoc_controlled, T_binned_controlled] = ...
    adaptive_req.analysis.compute_groupwise_feature_q_associations( ...
        T_step, ...
        feature_vars, ...
        'QVar', 'q_mean', ...
        'GroupVars', controlled_group_vars, ...
        'NumBins', 5, ...
        'BinMode', 'equal_count', ...
        'MinN', 6, ...
        'Verbose', true);

%% Display compact summaries

fprintf('\nGlobal associations.\n');
disp(T_assoc_global);

fprintf('\nAssociations grouped by WaveModel and M.\n');
disp(T_assoc_by_model_M);

fprintf('\nControlled associations. First rows.\n');
disp(head(T_assoc_controlled, min(20, height(T_assoc_controlled))));

%% Save outputs

save(fullfile(analysis_dir, 'level_04_feature_q_associations.mat'), ...
    'T_mc', ...
    'MC', ...
    'PATHS', ...
    'COL', ...
    'feature_vars', ...
    'T_step', ...
    'T_assoc_global', ...
    'T_binned_global', ...
    'T_assoc_by_model_M', ...
    'T_binned_by_model_M', ...
    'T_assoc_controlled', ...
    'T_binned_controlled', ...
    '-v7.3');

writetable(T_assoc_global, ...
    fullfile(analysis_dir, 'assoc_global.csv'));

writetable(T_binned_global, ...
    fullfile(analysis_dir, 'binned_global.csv'));

writetable(T_assoc_by_model_M, ...
    fullfile(analysis_dir, 'assoc_by_WaveModel_M.csv'));

writetable(T_binned_by_model_M, ...
    fullfile(analysis_dir, 'binned_by_WaveModel_M.csv'));

writetable(T_assoc_controlled, ...
    fullfile(analysis_dir, 'assoc_controlled_WaveModel_M_f0_cs.csv'));

writetable(T_binned_controlled, ...
    fullfile(analysis_dir, 'binned_controlled_WaveModel_M_f0_cs.csv'));

fprintf('\nSaved Level 4 association outputs to:\n%s\n', analysis_dir);

fprintf('\nLevel 4 feature-q association analysis completed.\n');
