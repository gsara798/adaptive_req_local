%% analyze_test_06_level07_train_model1_features_only.m
% Level 7: Train Model 1.
%
% Model 1:
%
%   q_local = F(local features)
%
% Predictors:
%
%   ang_entropy
%   radial_entropy
%
% This model does not use f0, M, cs_bg, WaveModel, or Omega as predictors.
% Those variables are only kept as metadata for later error analysis.

clear; clc; close all;
format compact;

%% Project setup

this_file = mfilename('fullpath');
this_dir = fileparts(this_file);
root_dir = fileparts(fileparts(this_dir));

addpath(root_dir);
root_dir = setup_adaptive_req();

%% Load MC results

experiment_name = 'test_06_feature_q_baseline';

[T_mc, MC, PATHS] = adaptive_req.analysis.load_mc_results( ...
    experiment_name, ...
    'RootDir', root_dir, ...
    'Verbose', true);

%% Detect columns and local features

COL = adaptive_req.analysis.detect_mc_columns(T_mc);

feature_vars = adaptive_req.analysis.detect_feature_variables( ...
    T_mc, ...
    'Verbose', true);

% For Model 1, use only the two local entropy features.
model1_features = ["ang_entropy", "radial_entropy"];

for i = 1:numel(model1_features)
    if ~ismember(model1_features(i), string(T_mc.Properties.VariableNames))
        error('Required Model 1 feature not found: %s', model1_features(i));
    end
end

fprintf('\nModel 1 features:\n');
disp(model1_features(:));

%% Output folder

analysis_dir = fullfile(PATHS.analysis_dir, 'level_07_model1_features_only');

if ~exist(analysis_dir, 'dir')
    mkdir(analysis_dir);
end

%% Train Model 1

[MODEL1, T_pred_model1, T_metrics_model1] = ...
    adaptive_req.analysis.train_q_model_features_only( ...
        T_mc, ...
        model1_features, ...
        'QVar', COL.q, ...
        'SplitMode', 'condition', ...
        'ConditionVar', COL.condition, ...
        'TrainFraction', 0.70, ...
        'RandomSeed', 1701, ...
        'ModelTypes', ["linear", "quadratic", "knn"], ...
        'KNNK', 15, ...
        'ClipPredictions', true, ...
        'Verbose', true);

%% Plot performance

FIGS = adaptive_req.analysis.plot_q_model_performance( ...
    T_pred_model1, ...
    T_metrics_model1, ...
    'Split', 'test', ...
    'SaveFigure', true, ...
    'OutputDir', analysis_dir, ...
    'BaseName', 'model1_features_only', ...
    'SavePNG', true, ...
    'SavePDF', true, ...
    'SaveFIG', false, ...
    'CloseAfterSave', false);

%% Print test metrics only

fprintf('\nModel 1 test metrics.\n');

T_test_metrics = T_metrics_model1(T_metrics_model1.split == "test", :);

disp(T_test_metrics);

%% Save outputs

save(fullfile(analysis_dir, 'level_07_model1_features_only.mat'), ...
    'T_mc', ...
    'MC', ...
    'PATHS', ...
    'COL', ...
    'feature_vars', ...
    'model1_features', ...
    'MODEL1', ...
    'T_pred_model1', ...
    'T_metrics_model1', ...
    'T_test_metrics', ...
    'FIGS', ...
    '-v7.3');

writetable(T_metrics_model1, ...
    fullfile(analysis_dir, 'model1_metrics.csv'));

writetable(T_test_metrics, ...
    fullfile(analysis_dir, 'model1_test_metrics.csv'));

writetable(T_pred_model1, ...
    fullfile(analysis_dir, 'model1_predictions.csv'));

fprintf('\nSaved Model 1 outputs to:\n%s\n', analysis_dir);

fprintf('\nLevel 7 Model 1 training completed.\n');
