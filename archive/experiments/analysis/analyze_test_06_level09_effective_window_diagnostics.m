%% analyze_test_06_level09_effective_window_diagnostics.m
% Level 9: Diagnose Model 1 errors versus effective window size.
%
% The goal is to test whether the apparent cs_bg dependence of the error is
% actually caused by changes in the number of true wavelengths inside the
% analysis window.

clear; clc; close all;
format compact;

%% Project setup

this_file = mfilename('fullpath');
this_dir = fileparts(this_file);
root_dir = fileparts(fileparts(this_dir));

addpath(root_dir);
root_dir = setup_adaptive_req();

%% Load Model 1 outputs

experiment_name = 'test_06_feature_q_baseline';

[~, ~, PATHS] = adaptive_req.analysis.load_mc_results( ...
    experiment_name, ...
    'RootDir', root_dir, ...
    'Verbose', true);

level07_dir = fullfile(PATHS.analysis_dir, 'level_07_model1_features_only');
level07_file = fullfile(level07_dir, 'level_07_model1_features_only.mat');

if ~exist(level07_file, 'file')
    error(['Model 1 file not found:\n%s\n', ...
           'Run analyze_test_06_level07_train_model1_features_only.m first.'], ...
           level07_file);
end

S = load(level07_file);

T_pred_model1 = S.T_pred_model1;
T_metrics_model1 = S.T_metrics_model1;

%% Add effective window diagnostics

cs_guess = 3.0;

T_pred_eff = adaptive_req.analysis.add_effective_window_metrics( ...
    T_pred_model1, ...
    'MVar', 'REQ_M', ...
    'F0Var', 'SIM_f0', ...
    'CsTrueVar', 'SIM_cs_bg', ...
    'CsGuess', cs_guess);

%% Output folder

analysis_dir = fullfile(PATHS.analysis_dir, ...
    'level_09_effective_window_diagnostics');

if ~exist(analysis_dir, 'dir')
    mkdir(analysis_dir);
end

%% Summarize errors versus effective window size

T_err_Meff = adaptive_req.analysis.summarize_q_model_errors_by_group( ...
    T_pred_eff, ...
    'GroupVars', 'M_eff_true_diag', ...
    'Split', 'test');

T_err_Meff_cs = adaptive_req.analysis.summarize_q_model_errors_by_group( ...
    T_pred_eff, ...
    'GroupVars', ["REQ_M", "SIM_cs_bg", "M_eff_true_diag"], ...
    'Split', 'test');

T_err_M_cs = adaptive_req.analysis.summarize_q_model_errors_by_group( ...
    T_pred_eff, ...
    'GroupVars', ["REQ_M", "SIM_cs_bg"], ...
    'Split', 'test');

%% Plot error versus M_eff

metrics_to_plot = ["RMSE", "MAE", "bias", "R2"];

for i = 1:numel(metrics_to_plot)

    metric_i = metrics_to_plot(i);

    adaptive_req.analysis.plot_q_model_error_by_group( ...
        T_err_Meff, ...
        'M_eff_true_diag', ...
        'Metric', metric_i, ...
        'Title', sprintf('Model 1 %s by effective window size', metric_i), ...
        'SaveFigure', true, ...
        'OutputDir', analysis_dir, ...
        'BaseName', sprintf('model1_%s_by_M_eff_true', metric_i), ...
        'SavePNG', true, ...
        'SavePDF', true, ...
        'SaveFIG', false, ...
        'CloseAfterSave', false);
end

%% Print diagnostic tables

fprintf('\nOverall Model 1 test metrics.\n');
disp(T_metrics_model1(T_metrics_model1.split == "test", :));

fprintf('\nModel 1 errors by effective window size.\n');
disp(sortrows(T_err_Meff, {'model_type', 'M_eff_true_diag'}));

fprintf('\nModel 1 errors by M, cs_bg, and effective window size.\n');
disp(sortrows(T_err_Meff_cs, {'model_type', 'REQ_M', 'SIM_cs_bg'}));

%% Save outputs

save(fullfile(analysis_dir, 'level_09_effective_window_diagnostics.mat'), ...
    'T_pred_model1', ...
    'T_pred_eff', ...
    'T_metrics_model1', ...
    'T_err_Meff', ...
    'T_err_Meff_cs', ...
    'T_err_M_cs', ...
    'cs_guess', ...
    '-v7.3');

writetable(T_err_Meff, ...
    fullfile(analysis_dir, 'model1_errors_by_M_eff_true.csv'));

writetable(T_err_Meff_cs, ...
    fullfile(analysis_dir, 'model1_errors_by_M_cs_Meff.csv'));

writetable(T_err_M_cs, ...
    fullfile(analysis_dir, 'model1_errors_by_M_cs.csv'));

fprintf('\nSaved effective-window diagnostics to:\n%s\n', analysis_dir);

fprintf('\nLevel 9 effective-window diagnostics completed.\n');