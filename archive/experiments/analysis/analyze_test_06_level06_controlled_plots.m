%% analyze_test_06_level06_controlled_plots.m
% Level 6 controlled visual analysis of q-feature relations.
%
% This script generates plots that support the feature-q conclusions by
% fixing selected parameters and plotting q versus feature in controlled
% subsets.

clear; clc; close all;
format compact;

%% Project setup

this_file = mfilename('fullpath');
this_dir = fileparts(this_file);
root_dir = fileparts(fileparts(this_dir));

addpath(root_dir);
root_dir = setup_adaptive_req();

%% Load MC results

experiment_name = 'test_06_feature_q_baseline';

[T_mc, MC, PATHS] = adaptive_req.analysis.load_mc_results( ...
    experiment_name, ...
    'RootDir', root_dir, ...
    'Verbose', true);

%% Detect columns and features

COL = adaptive_req.analysis.detect_mc_columns(T_mc);

feature_vars = adaptive_req.analysis.detect_feature_variables( ...
    T_mc, ...
    'Verbose', true);

%% Build T_step

T_step = adaptive_req.analysis.make_step_summary_table( ...
    T_mc, ...
    COL, ...
    feature_vars);

%% Output folder

plot_dir = fullfile(PATHS.analysis_dir, 'level_06_controlled_plots');

if ~exist(plot_dir, 'dir')
    mkdir(plot_dir);
end

%% ------------------------------------------------------------------------
% Plot set A
% Fix f0 and cs_bg, compare WaveModel and M
% -------------------------------------------------------------------------

target_f0 = 500;
target_cs = 3.0;

T_A = T_step(T_step.SIM_f0 == target_f0 & T_step.SIM_cs_bg == target_cs, :);

adaptive_req.analysis.plot_q_vs_feature_controlled( ...
    T_A, ...
    'ang_entropy', ...
    'GroupVars', ["SIM_WaveModel", "REQ_M"], ...
    'Title', sprintf('q vs angular entropy at f0 = %.0f Hz, cs = %.1f m/s', ...
        target_f0, target_cs), ...
    'SaveFigure', true, ...
    'OutputDir', plot_dir, ...
    'BaseName', 'A_q_vs_ang_entropy_fixed_f0_cs');

adaptive_req.analysis.plot_q_vs_feature_controlled( ...
    T_A, ...
    'radial_entropy', ...
    'GroupVars', ["SIM_WaveModel", "REQ_M"], ...
    'Title', sprintf('q vs radial entropy at f0 = %.0f Hz, cs = %.1f m/s', ...
        target_f0, target_cs), ...
    'SaveFigure', true, ...
    'OutputDir', plot_dir, ...
    'BaseName', 'A_q_vs_radial_entropy_fixed_f0_cs');

%% ------------------------------------------------------------------------
% Plot set B
% Fix M and cs_bg, compare WaveModel and f0
% -------------------------------------------------------------------------

target_M = 3;
target_cs = 3.0;

T_B = T_step(T_step.REQ_M == target_M & T_step.SIM_cs_bg == target_cs, :);

adaptive_req.analysis.plot_q_vs_feature_controlled( ...
    T_B, ...
    'ang_entropy', ...
    'GroupVars', ["SIM_WaveModel", "SIM_f0"], ...
    'Title', sprintf('q vs angular entropy at M = %.0f, cs = %.1f m/s', ...
        target_M, target_cs), ...
    'SaveFigure', true, ...
    'OutputDir', plot_dir, ...
    'BaseName', 'B_q_vs_ang_entropy_fixed_M_cs');

adaptive_req.analysis.plot_q_vs_feature_controlled( ...
    T_B, ...
    'radial_entropy', ...
    'GroupVars', ["SIM_WaveModel", "SIM_f0"], ...
    'Title', sprintf('q vs radial entropy at M = %.0f, cs = %.1f m/s', ...
        target_M, target_cs), ...
    'SaveFigure', true, ...
    'OutputDir', plot_dir, ...
    'BaseName', 'B_q_vs_radial_entropy_fixed_M_cs');

%% ------------------------------------------------------------------------
% Plot set C
% Fix M and f0, compare WaveModel and cs_bg
% -------------------------------------------------------------------------

target_M = 3;
target_f0 = 500;

T_C = T_step(T_step.REQ_M == target_M & T_step.SIM_f0 == target_f0, :);

adaptive_req.analysis.plot_q_vs_feature_controlled( ...
    T_C, ...
    'ang_entropy', ...
    'GroupVars', ["SIM_WaveModel", "SIM_cs_bg"], ...
    'Title', sprintf('q vs angular entropy at M = %.0f, f0 = %.0f Hz', ...
        target_M, target_f0), ...
    'SaveFigure', true, ...
    'OutputDir', plot_dir, ...
    'BaseName', 'C_q_vs_ang_entropy_fixed_M_f0');

adaptive_req.analysis.plot_q_vs_feature_controlled( ...
    T_C, ...
    'radial_entropy', ...
    'GroupVars', ["SIM_WaveModel", "SIM_cs_bg"], ...
    'Title', sprintf('q vs radial entropy at M = %.0f, f0 = %.0f Hz', ...
        target_M, target_f0), ...
    'SaveFigure', true, ...
    'OutputDir', plot_dir, ...
    'BaseName', 'C_q_vs_radial_entropy_fixed_M_f0');

%% Save workspace

save(fullfile(plot_dir, 'level_06_controlled_plots.mat'), ...
    'T_mc', ...
    'MC', ...
    'PATHS', ...
    'COL', ...
    'feature_vars', ...
    'T_step', ...
    'T_A', ...
    'T_B', ...
    'T_C', ...
    '-v7.3');

fprintf('\nSaved Level 6 controlled plots to:\n%s\n', plot_dir);
fprintf('\nLevel 6 controlled visual analysis completed.\n');