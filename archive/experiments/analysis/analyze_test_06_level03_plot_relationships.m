%% analyze_test_06_level03_plot_relationships.m
% Level 3 exploratory plots for test_06_feature_q_baseline.
%
% This script makes the first relationship plots:
%
%   1. q versus Omega
%   2. feature versus Omega
%   3. q versus feature
%
% The plots use T_step, where each row represents one condition-step group.

clear; clc; close all;
format compact;

%% Project setup

this_file = mfilename('fullpath');
this_dir = fileparts(this_file);
root_dir = fileparts(fileparts(this_dir));

addpath(root_dir);
root_dir = setup_adaptive_req();

%% Load MC results

experiment_name = 'test_06_feature_q_baseline';

[T_mc, MC, PATHS] = adaptive_req.analysis.load_mc_results( ...
    experiment_name, ...
    'RootDir', root_dir, ...
    'Verbose', true);

vars = string(T_mc.Properties.VariableNames);

for i = 1:numel(vars)
    fprintf('%3d  %s\n', i, vars(i));
end
%% Detect columns and features

COL = adaptive_req.analysis.detect_mc_columns(T_mc);

feature_vars = adaptive_req.analysis.detect_feature_variables( ...
    T_mc, ...
    'Verbose', true);
%% Build summary table

T_step = adaptive_req.analysis.make_step_summary_table( ...
    T_mc, ...
    COL, ...
    feature_vars);

fprintf('\nT_step rows: %d\n', height(T_step));

%% Output folder

plot_dir = fullfile(PATHS.analysis_dir, 'level_03_relationship_plots');

if ~exist(plot_dir, 'dir')
    mkdir(plot_dir);
end

%% Plot 1: q versus Omega

T_omega = adaptive_req.analysis.make_omega_group_summary(T_step);

adaptive_req.analysis.plot_q_vs_omega_grouped( ...
    T_omega, ...
    'ColorBy', 'REQ_M', ...
    'FacetBy', 'SIM_WaveModel', ...
    'UseErrorBars', true, ...
    'Title', 'Average q versus aperture', ...
    'SaveFigure', true, ...
    'OutputDir', plot_dir, ...
    'BaseName', 'q_vs_omega_average_by_M_WaveModel');


%% Plot 2 and 3: feature relationships

n_features_to_plot = min(6, numel(feature_vars));

for i = 1:n_features_to_plot

    feature_i = feature_vars(i);

    fprintf('\nPlotting feature: %s\n', feature_i);

    adaptive_req.analysis.plot_feature_vs_omega_grouped( ...
        T_step, ...
        feature_i, ...
        'ColorBy', 'REQ_M', ...
        'FacetBy', 'SIM_WaveModel', ...
        'UseErrorBars', true, ...
        'Title', sprintf('%s versus aperture', feature_i), ...
        'SaveFigure', true, ...
        'OutputDir', plot_dir, ...
        'BaseName', sprintf('feature_vs_omega_%s', feature_i), ...
        'SavePNG', true, ...
        'SavePDF', true, ...
        'SaveFIG', false, ...
        'CloseAfterSave', false);

    adaptive_req.analysis.plot_q_vs_feature_grouped( ...
        T_step, ...
        feature_i, ...
        'ColorBy', 'REQ_M', ...
        'FacetBy', 'SIM_WaveModel', ...
        'ShowFit', true, ...
        'Title', sprintf('q versus %s', feature_i), ...
        'SaveFigure', true, ...
        'OutputDir', plot_dir, ...
        'BaseName', sprintf('q_vs_feature_%s', feature_i), ...
        'SavePNG', true, ...
        'SavePDF', true, ...
        'SaveFIG', false, ...
        'CloseAfterSave', false);

end

%% Save workspace outputs

save(fullfile(plot_dir, 'level_03_plot_relationships.mat'), ...
    'T_mc', ...
    'MC', ...
    'PATHS', ...
    'COL', ...
    'feature_vars', ...
    'T_step', ...
    '-v7.3');

fprintf('\nSaved Level 3 plots to:\n%s\n', plot_dir);

fprintf('\nLevel 3 relationship plots completed.\n');
