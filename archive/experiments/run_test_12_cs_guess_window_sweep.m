%% run_test_12_cs_guess_window_sweep.m
% Generate the Test 12 dataset: local/global REQ features with a sweep over
% cs_guess to decouple nominal M from effective window size.

clear; clc; close all;
format compact;

%% Project setup

this_file = mfilename('fullpath');
experiments_dir = fileparts(this_file);
root_dir = fileparts(experiments_dir);

addpath(root_dir);
root_dir = setup_adaptive_req();

%% Run complete Test 12 Monte Carlo sweep

[T_mc, MC] = adaptive_req.studies.run_mc_sweep( ...
    'test_12_cs_guess_window_sweep', ...
    'RootDir', root_dir, ...
    'MaxConditions', Inf, ...
    'SaveResults', true, ...
    'SaveIntermediate', false, ...
    'ContinueOnError', false, ...
    'Verbose', true);

%% Validate and summarize results

assert(all(MC.T_status.success), ...
    'Test 12 dataset generation completed with failed conditions.');

assert(height(T_mc) == MC.expected_rows_selected, ...
    'Generated row count does not match the expected row count.');

required_vars = { ...
    'req_mapping', ...
    'global_req_mapping', ...
    'q_theory', ...
    'q_global_theory', ...
    'q_local_minus_global', ...
    'global_radial_entropy', ...
    'global_ang_entropy', ...
    'global_ecum_width_50_rel', ...
    'REQ_M', ...
    'REQ_cs_guess', ...
    'SIM_f0', ...
    'SIM_cs_bg'};

missing_vars = setdiff(required_vars, T_mc.Properties.VariableNames);

assert(isempty(missing_vars), ...
    'Missing Test 12 required variables: %s', strjoin(missing_vars, ', '));

if ~all(ismember({'M_eff_guess', 'M_eff_true_diag'}, ...
        T_mc.Properties.VariableNames))
    warning(['M_eff_guess or M_eff_true_diag not found. ', ...
        'Add these diagnostics before Level 12 analysis.']);
end

assert(~ismember('req_curve', T_mc.Properties.VariableNames), ...
    'The generated dataset unexpectedly contains the heavy req_curve.');

fprintf('\nTest 12 dataset completed successfully.\n');
fprintf('Test name           : test_12_cs_guess_window_sweep\n');
fprintf('Conditions generated: %d\n', MC.n_conditions_selected);
fprintf('Rows generated      : %d\n', height(T_mc));
fprintf('Unique SIM_f0       : %s\n', mat2str(unique(T_mc.SIM_f0).'));
fprintf('Unique SIM_cs_bg    : %s\n', mat2str(unique(T_mc.SIM_cs_bg).'));
fprintf('Unique REQ_M        : %s\n', mat2str(unique(T_mc.REQ_M).'));
fprintf('Unique REQ_cs_guess : %s\n', mat2str(unique(T_mc.REQ_cs_guess).'));
fprintf('Output folder:\n%s\n', MC.SAVE.output_dir);

